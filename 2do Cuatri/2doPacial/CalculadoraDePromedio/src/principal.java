    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double notas = 0, contador = 0;
        double promedio;
        String ape;

        for (int i = 0; i < nomina.size(); i++) {
            ape = nomina.get(i).getApellido();
            if (ape.equals(String.valueOf(cmbAlumno.getSelectedItem()))) {
                notas += nomina.get(i).getNota();
                contador++;
            }
        }
        promedio = notas / contador;
        if(!(promedio>0 || promedio<10000000)){
            txtPromedio.setText("");
        }else{ txtPromedio.setText(String.valueOf(promedio));
        }
       
    }//GEN-LAST:event_btnCalcularActionPerformed

