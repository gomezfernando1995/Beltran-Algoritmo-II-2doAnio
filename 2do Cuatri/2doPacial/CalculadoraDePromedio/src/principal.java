    private void btnAnotarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnotarseActionPerformed

        // CREO UN OBJETO ALUMNO y ARRAY STRING
        Alumno altaAlumno = new Alumno();
        String addAlumnoLista[] = new String[6];

        /**
         * ****** RELLENAR EL OBJETO Y CARGARLO EN EL ARRAYLIST* **
         */
        //Relleno el objeto con los datos de cada TextField
        altaAlumno.setLegajo(Integer.parseInt(txtLegajo.getText()));
        altaAlumno.setNombre(txtNombre.getText());
        altaAlumno.setApellido(txtApellido.getText());
        altaAlumno.setEmail(txtEmail.getText());
        altaAlumno.setMateria(String.valueOf(txtMateria.getSelectedItem()));
        altaAlumno.setNota(Integer.parseInt(txtNota.getText()));
        //Agrego el objeto al ArrayList
        nomina.add(altaAlumno);

        /**
         * ****** RELLENAR ARRAY STRING Y SETEARLO EN TABLA INTERFAZ *******
         */
        addAlumnoLista[0] = altaAlumno.getNombre();
        addAlumnoLista[1] = altaAlumno.getApellido();
        addAlumnoLista[2] = altaAlumno.getEmail();
        addAlumnoLista[3] = String.valueOf(altaAlumno.getLegajo());
        addAlumnoLista[4] = altaAlumno.getMateria();
        addAlumnoLista[5] = String.valueOf(altaAlumno.getNota());
        //Cargo la Tabla Interfaz con el Array String
        modelo.addRow(addAlumnoLista);

        /**
         * ********** RELLENAR ComboBox CON APELLIDO SIN REPETIR **************
         */
        if (!existe(addAlumnoLista[1])) {
            cmbAlumno.addItem(addAlumnoLista[1]);
        }
    }//GEN-LAST:event_btnAnotarseActionPerformed

    public boolean existe(String apellido) {
        int size = cmbAlumno.getItemCount();
        for (int i = 0; i < size; i++) {
            String item = cmbAlumno.getItemAt(i);
            if (item.equals(apellido)) {
                return true;
            }
        }
        return false;
    }


