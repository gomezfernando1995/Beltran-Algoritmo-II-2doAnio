    private void buscar() {
        for (int i = 0; i < aPersonas.size(); i++) {

            if (aPersonas.get(i).getNombre().equalsIgnoreCase(txtNombre.getText())) {
                txtNombre.setEnabled(false);
                txtMail.setText(aPersonas.get(i).getMail());
                    txtTel.setText(aPersonas.get(i).getTelefono());
                posicionArray = i;
                flag = 1;

                if (btnConsulta.isSelected()) {
                    
                    btnAplicar.setEnabled(false);

                }
                if (btnBaja.isSelected()) {
                    btnAplicar.setEnabled(true);
                 
                    JOptionPane.showMessageDialog(null, "Encontrado");

                }
                if (btnModificar.isSelected()) {
                   
                    btnAplicar.setEnabled(true);
                    txtNombre.setEnabled(true);
                    txtMail.setEnabled(true);
                    txtTel.setEnabled(true);
                }

            }

        }

    }


        
        
        
    private void txtNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyPressed
        char c = (char) evt.getKeyCode();
        if (c == evt.VK_ENTER) {
            buscar();
        }
    }//GEN-LAST:event_txtNombreKeyPressed

