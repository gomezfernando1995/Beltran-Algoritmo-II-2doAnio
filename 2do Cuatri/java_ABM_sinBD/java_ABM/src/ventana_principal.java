    private void btnAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarActionPerformed
        if (btnAlta.isSelected()) {

            if (txtNombre.getText().isEmpty() || txtMail.getText().isEmpty() || txtTel.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Se debe completar todos los campos", "Alert", JOptionPane.WARNING_MESSAGE);

            } else {

                buscar();

                if (flag == 1) {
                    JOptionPane.showMessageDialog(null, "Ya existe este usuario");
                    txtNombre.setEnabled(true);
                    flag=0;
                } else {
                    Personas p1 = new Personas(txtNombre.getText(), txtMail.getText(), txtTel.getText());
                    aPersonas.add(p1);
                    limpiarTextos();
                    JOptionPane.showMessageDialog(null, "Alta exitosa");
                    txtNombre.requestFocus();
                }

            }

        }
        if (btnBaja.isSelected()) {

            aPersonas.remove(posicionArray);
            limpiarTextos();
            btnAplicar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Baja exitosa");

        }
        if (btnModificar.isSelected()) {
            Personas p1 = new Personas(txtNombre.getText(), txtMail.getText(), txtTel.getText());

            aPersonas.set(posicionArray, p1);
            btnAplicar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Modificacion exitosa");
            limpiarTextos();
            txtMail.setEnabled(true);
            txtTel.setEnabled(true);
            txtNombre.requestFocus();

        }


    }//GEN-LAST:event_btnAplicarActionPerformed

