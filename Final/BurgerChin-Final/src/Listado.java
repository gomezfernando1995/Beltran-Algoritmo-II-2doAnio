    private void llenarTabla() { 
        try {
            String vec[] = new String[4];
            st = cx.prepareStatement("SELECT * FROM productos ");
            rs = st.executeQuery();

            while (rs.next()) {
                // CARGAMOS EL VECTOR CON DATOS OBTENIDOS
                vec[0] = rs.getString("idproducto");
                vec[1] = rs.getString("tipo");
                vec[2] = rs.getString("descripcion");
                vec[3] = rs.getString("precio");
                modelo.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Listado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
     //SET DE TABLA DE INTERFAZ
